function assignGrade(p){
    if (p>=90){
        console.log('a')
    }
    else if(p>=80 && p<=89){
        console.log("b")
    }
    else if(p>=70 && p<=79){
        console.log("c")
    }
    else if(p>=60 && p<=69){
        console.log("d")
    }
    else{
        console.log('e')
    }
}
assignGrade(12)
